// Variable Types
var num = 25          // Mutable variable
let pi = 3.14159    // Immuatable constant

// Specifying Data Types
var count: Int = 100                // Note: Uppercase Data Types
var name: String = "Ellen"
var gpa: Double = 1.23

// Changing Variables
num = 30                  // Works perfectly because x is mutable
                        // pi = 3.14 will crash
                       
// Printing Variables
print(num)                    // Prints 30
print("Name: ", name)       // Print print outside of string, separated with comma
print("pi: \(pi)")          // Can print variables in string with \(var)

